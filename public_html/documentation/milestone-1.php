		<h2>Purpose, Audience &amp; Goal:</h2>
		<p><strong>Purpose:</strong>
			This project will be a professional portfolio website with a focus on front-end web development concepts such as UX/UI.
		</p>
		<p>
			<strong>Audience:</strong>
			This site is designed with a professional audience in mind.  Targeting individuals in the web development and I.T. community nationwide.
			Target audience to include: startup founders, hiring managers, recruiters, and peers.
		</p>
		<p>
			<strong>Goal:</strong>
			The primary goal of this site is to provide users with a direct way to contact and communicate with me in regards to employment and collaboration opportunities
			through the contact form, and/or connected social media profiles.  The secondary goal of this site is to showcase my work, and to contribute to a polished professional online presence.
		</p>
		<h2>Persona</h2>
		<p>
			<strong>Name:</strong>
			Bill Davis
		</p>
		<p>
			<strong>Age:</strong>
			38
		</p>
		<p>
			<strong>Profession:</strong>
			Hiring manager at SolarCell, a startup that is looking to hire a full-time, in house Front-End Web Developer.  They have received funding from a few major corporations and are ready to
			expand their website offerings to better penetrate the tech and satellite market.  Their product line consists of deep space solar cells that power spacecraft and satellites.  They also offer wing
			mounted solar cell systems for use in the ever expanding drone sector.
		</p>
		<p><strong>Technology:</strong>
			Apple Guru.  Works on an iMac Pro connected to a 27" LG UltraFine 5k display while in the office, a MacBook Pro while at home and keeps up to date on the go using an iPhone X.
		</p>
		<p><strong>Attitudes and behaviors:</strong>
			Spends a lot of time browsing the web and keeping up to date on current events.  Especially as they relate to the tech industry.  Heavily involved with social media and believes someone's
			online presences is the most important thing when looking to hire someone.  A good resume exhibits organizational skills but he rarely spends much time looking over them.  He feels
			interacting with potential hires is the next best way to get a feel for them.  He wants to make sure personality matches online presence and style.
		</p>
		<p><strong>Frustrations and Needs:</strong>
			Bill is looking for someone who can grow with the company.  He is not looking for a senior level developer, but someone who has 1-2 years of real working experience as well as some
			formal education related to the field such as BS in computer sciences, physics, math, engineering or similar field.  Someone who can be mentored and trained to work within the company's
			culture of high tech hardware and software.  He has been getting some resumes, but no one has really stood out of the crowd.  He frequently visits local networking and community events
			in hopes of finding someone who can present well in a short informal setting.  He is also open to any recommendations from his colleagues and friends with in the community.
		</p>
		<p><strong>Goals:</strong>
			Bill is all about online presence!  Does this candidate have a professional LinkedIn profile? How about twitter?  How about a GitHub profile with detailed repositories?
			In our ever increasing digital world, he sees a resume as secondary to how well your site presents itself.  Well written content is all good but if it's not presented in a clean concise
			fashion than it might as well be blank.  Bill needs to know pretty swiftly if this potential person is worth investing further research as well as setting up some sort of interaction.  Whether
			that be a phone conversation or a full blown interview.
		</p>
		<p><strong>User Story:</strong>
			As a user, I need to quickly assess whether this person presents content in a concise and appealing way as well as how involved they are in our local tech community.
		</p>
